// scripts/generate-articles-dart.js
// Reads all published articles from content/article/*.md and generates
// lib/data/articles_data.dart for the sio2_renovations_frontend Flutter project.
// Run locally: node scripts/generate-articles-dart.js
// Run in CI: triggered automatically by sync-articles.yml on push to main.

const fs = require('fs');
const path = require('path');
const matter = require('gray-matter');

const HUB_BASE_URL = 'https://hub.sio2renovations.com';
const REF = 'sio2renovations';
const ARTICLES_DIR = path.join(__dirname, '../content/article');
const OUTPUT_FILE = path.join(__dirname, '../generated/articles_data.dart');
const MAX_DESC_LENGTH = 90;

// Derives the card category badge from the article's tags array.
// Priority order: most specific first.
function categoryFromTags(tags = []) {
  if (tags.includes('loft') || tags.includes('atelier')) return 'LOFT & ATELIER';
  if (tags.includes('salle-de-bains'))                   return 'SALLE DE BAINS';
  if (tags.includes('cuisine'))                          return 'CUISINE';
  if (tags.includes('maison'))                           return 'MAISON';
  if (tags.includes('appartement'))                      return 'APPARTEMENT';
  if (tags.includes('combles'))                          return 'COMBLES';
  if (tags.includes('extension'))                        return 'EXTENSION';
  return 'ARTICLE';
}

// Truncates description to MAX_DESC_LENGTH at the last word boundary.
function truncate(str) {
  if (!str || str.length <= MAX_DESC_LENGTH) return str;
  const cut = str.lastIndexOf(' ', MAX_DESC_LENGTH);
  return str.slice(0, cut > 0 ? cut : MAX_DESC_LENGTH) + '...';
}

// Escapes double quotes and backslashes for Dart string literals.
function dartEscape(str) {
  return (str || '').replace(/\\/g, '\\\\').replace(/"/g, '\\"');
}

// Read and parse all markdown files
const files = fs.readdirSync(ARTICLES_DIR).filter(f => f.endsWith('.md'));

const articles = files
  .map(file => {
    const raw = fs.readFileSync(path.join(ARTICLES_DIR, file), 'utf8');
    const { data } = matter(raw);
    return data;
  })
  .filter(d => d.published === true)
  .sort((a, b) => new Date(b.date) - new Date(a.date));

if (articles.length === 0) {
  console.error('No published articles found.');
  process.exit(1);
}

// Generate Dart source
const entries = articles.map(a => {
  const category    = dartEscape(categoryFromTags(a.tags));
  const title       = dartEscape(a.title);
  const description = dartEscape(truncate(a.description));
  const image       = `${HUB_BASE_URL}${a.cover}`;
  const url         = `${HUB_BASE_URL}/articles/${a.slug}?ref=${REF}`;

  return `  {
    "category": "${category}",
    "title": "${title}",
    "description": "${description}",
    "image": "${image}",
    "url": "${url}",
  },`;
}).join('\n');

const dart = `// AUTO-GENERATED — do not edit manually.
// Source: hub_sio2renovations/content/article/*.md
// Regenerated by scripts/generate-articles-dart.js on every push to main.

const List<Map<String, String>> articlesData = [
${entries}
];
`;

fs.mkdirSync(path.dirname(OUTPUT_FILE), { recursive: true });
fs.writeFileSync(OUTPUT_FILE, dart, 'utf8');

console.log(`Generated ${articles.length} articles → ${OUTPUT_FILE}`);
